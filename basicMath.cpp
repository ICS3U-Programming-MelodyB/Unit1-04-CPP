// This program does basic math.
#include <iostream>
#include <cmath>

int main() {
  std::cout << "10 + 12 = " << 10+12 << std::endl;
  std::cout << "4 - 3 = " << 4-3 << std::endl;
  std::cout << "1200 / 2 = " << 1200/2 << std::endl;
  std::cout << "(100 - 50) / 2 = " << (100-50)/2 << std::endl;
  std::cout << "2^10 = " << pow(2, 10) << std::endl;
  std::cout << "27 / 3 = " << float(27)/float(3) << std::endl;
}
